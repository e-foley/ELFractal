<div class="section">The Website</div>
<div>Empyreal Light is a flame fractal gallery intended to spread my love for flame fractals to a wide audience.
The website began in May of 2007, when its address was empyreal-light.awardspace.com.
In August of 2008, the website was transferred to elfractal.com, the address it retains today.
</div>

<p />

<div class="section">The Name</div>
<div>The word <span style="font-style: italic;">empyreal</span>, pronounced <span style="font-style: italic;">em-PAHY-ree-uhl</span>, is defined by the Random House Unabridged Dictionary as an adjective "pertaining to the sky; celestial" or "formed of pure fire or light."
I find the word describes the dramatic colors and essential nature of flame fractals well, and so I use the word in the name of the website.</div>

